public class vowel {
    public static void main(String [] args)
    {
        char c='c';
        if(c=='a'||c=='A'||c=='e'||c=='E'||c=='i'||c=='I'||
        c=='o'||c=='O'||c=='u'||c=='U')
        System.out.println("Character is vowel "+c);
        else
        System.out.println("Character is not a vowel "+c);
    }
}
